// MATRIZ
// Uma matriz é como um array dentro de outro array, criando uma tabela ou grades de valores.
const matriz = [
    [1, 2, 3], // Linha 0
    [4, 5, 6], // Linha 1
    [7, 8, 9], // Linha 2
  ];
  
  // Para acessar uma matriz, você usa dois índices:
  // O primeiro é a linha e o segundo é a coluna.
  console.log(matriz[0][0]); // Mostra 1 (linha 0, coluna 0)
  console.log(matriz[1][2]); // Mostra 6 (linha 1, coluna 2)
  console.log(matriz[2][1]); // Mostra 8 (linha 2, coluna 1)
  
  // Matrizes são úteis para representar coisas como tabelas, tabuleiros ou grades de valores.
  