// ARRAY
// Um array é como uma lista organizada onde os valores ficam guardados um ao lado do outro.
// Cada valor tem um número (chamado índice) que começa do 0 e serve para acessar os valores facilmente.

// Geralmente, arrays são usados para armazenar dados do mesmo tipo.

// Representação de um array na memória:
const array = [10, 20, 30, 40, 50];
// Índices:     0   1   2   3   4

// Como acessar os valores de um array:
console.log(array[0]); // Mostra o valor 10 (índice 0)
console.log(array[1]); // Mostra o valor 20 (índice 1)
console.log(array[2]); // Mostra o valor 30 (índice 2)

